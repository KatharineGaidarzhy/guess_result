function pow(x, y) {
    let t = 1;  // Початкове значення для результату
    let i = 0;  // Лічильник
  
    // Цикл виконується y разів
    while (i < y) {
      t *= x;  // Множимо t на x
      i++;     // Збільшуємо лічильник
    }
  
    return t;  // Повертаємо результат
  }
  
  // Тепер функція приймає аргументи:
  console.log(pow(10, 2));  // Виведе 100 (10^2)
  console.log(pow(5, 4));   // Виведе 625 (5^4)
  